# importing dependencies
import numpy as np
import pandas as pd

credit_dataset = pd.read_csv('/content/credit_data.csv')
credit_dataset.head()

credit_dataset.shape

# distribution of the two classes
credit_dataset['Class'].value_counts()

"""THIS IS HIGHLY IMBALANCE DATASET:
  

*   0 means Legal Transaction
*   1 means Fraud Transaction


"""

# separating the legit and fraud transactions
legit = credit_dataset[credit_dataset.Class == 0]
fraud = credit_dataset[credit_dataset.Class == 1]

print(legit.shape, fraud.shape)

"""UNDER - SAMPLING

Build a sample dataset containing similar distribution of legit and fraud transactions

Number of fraud transaction:  224
"""

legit_sample = legit.sample(n = 224)

print(legit_sample.shape)

"""Concatenate the Two DataFrame"""

new_dataset = pd.concat([legit_sample, fraud], axis = 0)
print(new_dataset.head())

new_dataset['Class'].value_counts()

